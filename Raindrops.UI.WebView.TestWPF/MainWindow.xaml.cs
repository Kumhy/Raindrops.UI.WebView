namespace Raindrops.UI.WebView.TestWPF
{
    /// <summary>
    /// Interaction logic for MainWindow.xaml
    /// </summary>
    public partial class MainWindow : Window
    {
        public MainWindow()
        {
            InitializeComponent();
            Loaded += MainWindow_Loaded;
        }

        private void MainWindow_Loaded(object sender, RoutedEventArgs e)
        {
            Browser.WebView.LoadUrl("www.baidu.com");
        }

        public string Url
        {
            get { return (string)GetValue(UrlProperty); }
            set { SetValue(UrlProperty, value); }
        }

        public static readonly DependencyProperty UrlProperty =
            DependencyProperty.Register("Url", typeof(string), typeof(MainWindow), new PropertyMetadata("www.baidu.com"));
        private void Button_Click(object sender, RoutedEventArgs e)
        {
            string url = Url?.Trim();
            if (string.IsNullOrEmpty(url))
                return;
            Browser.WebView.LoadUrl(url);
        }
    }
}
